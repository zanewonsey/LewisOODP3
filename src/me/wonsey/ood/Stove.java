package me.wonsey.ood;

public class Stove
{
   public Stove()
   {
      
   }
   
   public void on()
   {
      System.out.println("Stove was turned on");
   }

   public void off()
   {
      System.out.println("Stove was turned off");
   }
   
   public void heatUp()
   {
      System.out.println("Stove starts to heat up");
   }
   
   public void coolDown()
   {
      System.out.println("Stove starts to cool down");
   }
}
